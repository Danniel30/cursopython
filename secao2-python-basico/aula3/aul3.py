"""
str - string
"""
print("Essa é uma 'string' (str) ")
print('Essa é uma "string" (str) ')
print("Esse é meu \"texto\" (str).")
print('Esse é meu \'texto\' (str).') # Usamos a função '\' barra invertida que se chama escape, ele faz que o python ignore as aspas.
print('Esse é meu \n (str).') # Usamos a função '\n' quebra de linha
print(r'Esse é meu \n (str).') # colocando r no inicio do argumento, o python entende que não é para executar nada dentro da string
